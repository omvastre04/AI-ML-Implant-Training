# Examples of Arithmetic Operators
print("\nArithmetic Operation :")
x=10
y=2
print("x=10, y=2")

print("x + y = ", x+y)
print("x - y = ", x-y)
print("x * y = ", x*y)
print("x / y = ", x/y)
print("x ** y= ", x**y)
print("x // y= ", x//y)
print("x % y = ", x%y)
print("------------------")

